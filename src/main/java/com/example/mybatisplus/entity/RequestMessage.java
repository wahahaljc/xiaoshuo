package com.example.mybatisplus.entity;

public class RequestMessage {
    /**
     * 消息内容
     */

    private String msg;





    /**
     * 获取 消息内容
     *
     * @return msg 消息内容
     */

    public String getMsg() {

        return this.msg;

    }



    /**
     * 设置 消息内容
     *
     * @param msg 消息内容
     */

    public void setMsg(String msg) {

        this.msg = msg;

    }

}
